def printBalanceSheetEntry(entry):
	print("{label:>20s}: {assets:>13,.2f} | {liabilities:>13,.2f} || {net:>13,.2f}".format(
		label = entry.name, 
		assets = entry.assets, liabilities = entry.liabilities, 
		net = entry.assets - entry.liabilities))

def printBalanceSheet(balanceSheetEntries, label):
	print("{label:>20s}: {assets:<13s} | {liabilities:<13s} || {net:<13s}".format(
		label = label, 
		assets = "Assets", liabilities = "Liabilities", 
		net = "Net value"))
		
	totals = BalanceSheetEntry("TOTALS")
	for entry in balanceSheetEntries:
		printBalanceSheetEntry(entry)
		totals.assets += entry.assets
		totals.liabilities += entry.liabilities

	totalsWithoutRekts = copy.copy(totals)
	totalsWithoutRekts.name = "TOTALS w/o Rekt CDPs"
	totalsWithoutRekts.assets -= balanceSheetEntries[-1].assets
	totalsWithoutRekts.liabilities -= balanceSheetEntries[-1].liabilities
		
	print("--------------------------------------------------------------------")
	printBalanceSheetEntry(totals)
	printBalanceSheetEntry(totalsWithoutRekts)
	print("====================================================================\n")
	return totalsWithoutRekts

