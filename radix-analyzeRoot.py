def callRadixGateway(method, params = None):
	method = method.strip('?%:')
	url = "{}/{}".format(gateway, method)
	payload = json.dumps(params)
	headers = {'content-type': "application/json", 'cache-control': "no-cache"}
	try:
		response = requests.request("POST", url, data=payload, headers=headers)  #, auth=(rpc_user, rpc_password))
# 		print("Radix Gateway raw response: {}".format(response.text))
		jsonResponse = json.loads(response.text)
		jsonErrorType = jsonResponse.get("error_type")
		jsonErrorMessage = jsonResponse.get("message")
		jsonErrorTrace = jsonResponse.get("trace_id")
		if jsonErrorType is not None:
			raise Exception("RPC call failed with error {errortype}, trace {trace}: {msg}".format(
				errortype=jsonErrorType, trace=jsonErrorTrace, msg=jsonErrorMessage))
		return jsonResponse
		
	except requests.exceptions.RequestException as e:
		print(e)
	except:
		print ('Radix Gateway RPC call failed, check if radix gateway is running on this machine')
		raise
		
def boolmoji(v): return "✅️" if v else "❌️"

class TypedValue:
	def __init(self):
		self.value = None
		self.kind = None
		self.type_name = None
		
	def __init__(self, value, kind, type_name):
		self.value = value
		self.kind = kind
		self.type_name = type_name
		
	def isDictKind(self):
		return self.kind == "Tuple" or self.kind == "Map" # TODO: Map should be treated differently
		
		
	def toString(self, indent=0):
		#result = "\t" * int(indent)
		printedValue = "??????"
		
		if self.isDictKind():
			printedValue = "** {} named entries **".format(len(self.value))
		elif self.kind == "Bool" and type(self.value) == bool:
			printedValue = boolmoji(self.value)
		else:
			printedValue = str(self.value)
		
		printedTypename = "{}: ".format(self.type_name) if self.type_name is not None else ""
		
		result = "({kind}) {typename}{value}".format(kind=self.kind, typename=printedTypename, value=printedValue)
		if self.isDictKind():
			indent += 1
			for k, v in self.value.items():
				result += "\n" + "\t"*indent + "{k} = {v}".format(k=k, v=v.toString(indent))
		return result
		
	def __str__(self):
		return self.toString()
		
	def __getitem__(self, idx):
		return self.value[idx]
				
		
		
def parseTypedValue(jsonIn):
	#print("{} {}".format(jsonIn.get("kind"), jsonIn.get("field_name")))
	kind = jsonIn["kind"]
	value = None
	if kind == "Tuple":
		value = parseFieldsList(jsonIn["fields"])
	elif kind == "Enum":
		value = jsonIn["fields"]
	elif kind == "Map":
		value = parseMap(jsonIn["entries"])
	else:
		value = jsonIn["value"]
			
	return TypedValue(value = value, kind = kind, type_name = jsonIn.get("type_name"))


def parseFieldsList(jsonIn):
	result = {}
	for f in jsonIn:
		result[f["field_name"]] = parseTypedValue(f)
	return result
	
